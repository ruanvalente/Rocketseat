/*
Em javascript usamos os loops quando precisamos repetir uma tarefa diversas vezes
temos o loop for, while e o do while
*/
// Loop for 

for ( var i = 0; i <= 10; i++ ) {
  console.log( i );
}

// Loop while

var count = 0;

console.log('======');

while ( count <= 10 ) {
  console.log( count );
  count++;
}